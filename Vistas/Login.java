    private void btnLogin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogin1ActionPerformed
       
        
        //1. Capturamos los valores que ingresa el usuario
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        //2. Mostramos los valores por consola
        System.out.println("Username: " + username + ", password: " + password);
       
        // vaalidamos con los campos de login
        if(username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar", username, JOptionPane.WARNING_MESSAGE);
        }else{          
            // inicio de sesion exitoso
            if(username.equals("MisionTIC") && password.equals("Ciclo2")){
                JOptionPane.showMessageDialog(this,"Bienvenido al Sistema");
                //crear un instancia del Jframe UserMenu
                UserMenu userMenu = new UserMenu();
                userMenu.setVisible(true);
                //cerramos la ventana de login
                this.dispose();
               
                
            }else{
                JOptionPane.showMessageDialog(this,"No pudo iniciar sesion", username, JOptionPane.ERROR_MESSAGE);
            
            
            }
        
        }
    }//GEN-LAST:event_btnLogin1ActionPerformed
  
    
    
